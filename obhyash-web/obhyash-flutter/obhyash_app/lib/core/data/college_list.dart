/// Returns up to 8 canonical college names matching [query].
List<String> searchColleges(String query) {
  final q = query.toLowerCase().trim();
  if (q.isEmpty) return [];

  final seen = <String>{};
  final results = <String>[];

  for (final entry in _collegeData) {
    final name = entry['name'] as String;
    if (seen.contains(name)) continue;

    final nameMatch = name.toLowerCase().contains(q);
    final aliasMatch = (entry['search'] as List<String>).any(
      (s) => s.contains(q),
    );

    if (nameMatch || aliasMatch) {
      seen.add(name);
      results.add(name);
      if (results.length >= 8) break;
    }
  }

  return results;
}